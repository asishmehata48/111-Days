// Function declaration and calling
function greet(name) {
    console.log("Hello, " + name + "!");
}
greet("World");

// Function with parameters and return value
function add(a, b) {
    return a + b;
}
let sum = add(3, 4);
console.log("Sum:", sum);

// Arrow function
const multiply = (x, y) => x * y;
console.log("Product:", multiply(5, 6));
